    public void pasarDatos() {
        try {
            AdministrarProductos ap = new AdministrarProductos();
            MenuVentas.escritorio.removeAll();
            MenuVentas.escritorio.add(ap);
            ap.txtCodigo.setText(String.valueOf(tblTab.getValueAt(tblTab.getSelectedRow(), 0)));
            ap.verificacionCodigos();
            ap.setVisible(true);
            this.dispose();
        } catch (Exception e) {
        }

    }

