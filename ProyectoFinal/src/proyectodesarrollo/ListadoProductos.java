    public void cargarTablaProductos(String buscar) {
        try {
            String[] titulos = {"Código de Barras", "Nombre", "Marca", "Categoría", "", "Presentaciòn", "PVP", "IVA", "Stock"};
            String[] registros = new String[9];
            modelo = new DefaultTableModel(null, titulos);

            String sql = "";
            sql = "SELECT * FROM PRODUCTOS WHERE COD_PRO LIKE '%" + buscar + "%' OR NOM_PRO LIKE '%" + buscar + "%' OR MAR_PRO LIKE '%" + buscar + "%' OR NOM_CAT_P LIKE '%" + buscar + "%'";

            //sql = "SELECT * FROM PRODUCTOS WHERE NOM_PRO LIKE '%"+buscar+"%'";
            System.out.println(sql);
            conexion cc = new conexion();
            Connection cn = cc.conectar();
            Statement psd = cn.createStatement();
            ResultSet rs = psd.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("COD_PRO");
                registros[1] = rs.getString("NOM_PRO");
                registros[2] = rs.getString("MAR_PRO");
                registros[3] = rs.getString("NOM_CAT_P");
                //registros[4] = rs.getString("PRES_PRO");

                registros[4] = String.valueOf(mp.redondearDecimales(rs.getDouble("PRES_PRO"), 2));
                registros[5] = rs.getString("COD_UNI_PRES");
                //registros[6] = String.valueOf(rs.getString("PVP"));

                registros[6] = String.valueOf(mp.redondearDecimales(rs.getDouble("PVP"), 2));

                registros[7] = String.valueOf(rs.getString("IVA_PRO"));
                registros[8] = String.valueOf(rs.getString("STOCK_PRO"));
                modelo.addRow(registros);
            }

            tblTab.setModel(modelo);
            cn.close();
        } catch (SQLException ex) {
            Logger.getLogger(ListadoProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

